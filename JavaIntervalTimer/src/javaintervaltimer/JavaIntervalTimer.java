    int counter = 30;
    
    public void startTimer() {
        Timer timer = new Timer();
        counter = 30;
        
        TimerTask task = new TimerTask() {
            public void run() {
                timeField.setText(Integer.toString(counter));
                System.out.println(Integer.toString(counter));
                counter--;
                if(counter == - -1) {
                    timer.cancel();
                }
            }
        };
        timer.scheduleAtFixedRate(task, 1000, 1000);
    }

